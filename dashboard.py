    "# -----------------------------------\n",
    "\n",
    "model = YOLO(\"models/best.pt\")\n",
    "\n",
    "# -----------------------------------\n",
    "# HEADER\n",
    "# -----------------------------------\n",
    "\n",
    "st.markdown(\n",
    "\"\"\"\n",
    "<h1 style='text-align:center;color:yellow;'>\n",
    "Warehouse Management System\n",
    "</h1>\n",
    "<h3 style='text-align:center;color:orange;'>\n",
    "Bag Counting Management System\n",
    "</h3>\n",
    "\"\"\",\n",
